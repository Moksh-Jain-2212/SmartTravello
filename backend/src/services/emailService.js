// Send itinerary email
export const sendItineraryEmail = async (userEmail, trip, itineraryData, budgetData, weatherData) => {
  try {
    console.log(`[EmailService] Attempting to send email to: ${userEmail}`);
    
    if (!process.env.EMAIL_USER || !process.env.EMAIL_PASS) {
      throw new Error('Email credentials not configured. Please set EMAIL_USER and EMAIL_PASS environment variables.');
    }

    const transporter = createTransporter();
    
    const mailOptions = {
      from: process.env.EMAIL_USER,
      to: userEmail,
      subject: `✈️ Your Travel Itinerary for ${trip.destination}`,
      html: generateItineraryHTML(trip, itineraryData, budgetData, weatherData),
    };

    console.log('[EmailService] Sending email...');
    const result = await transporter.sendMail(mailOptions);
    console.log(`✅ Itinerary email sent to ${userEmail}, Message ID: ${result.messageId}`);
    return { success: true, messageId: result.messageId };
  } catch (error) {
    console.error('❌ Failed to send itinerary email:', error);
    throw new Error(`Email sending failed: ${error.message}`);
  }
};


